package com.ekino.trombihack.model;

public class Hobby {
    private String hobbyName;
    private String hobbyUrl;
    private String hobbyPic;

    public String getHobbyName() {
        return hobbyName;
    }

    public void setHobbyName(String hobbyName) {
        this.hobbyName = hobbyName;
    }

    public String getHobbyUrl() {
        return hobbyUrl;
    }

    public void setHobbyUrl(String hobbyUrl) {
        this.hobbyUrl = hobbyUrl;
    }

    public String getHobbyPic() {
        return hobbyPic;
    }

    public void setHobbyPic(String hobbyPic) {
        this.hobbyPic = hobbyPic;
    }
}